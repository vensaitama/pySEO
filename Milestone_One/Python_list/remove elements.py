# var.remove() 

fruits = ["banana", "watermelom", "cherry"]
fruits.remove("cherry")
print(fruits)

# del var[]

animals = ["dog", "cat", "frog", "cow", "cat"]
del animals [1]
print(animals)

# pop() Method
# delets the last one

animals = ["dog", "cat", "frog", "cow", "cat"]
animals.pop()
print(animals)

# var.pop(1)
# delets the mentioned index elements

animals = ["dog", "cat", "frog", "cow", "cat"]
animals.pop(2)
print(animals)

# var.clear() celears all the elements

animals = ["dog", "cat", "frog", "cow", "cat"]
animals.clear()
print(animals)